package com.comcast.pop.modules.xmlhelper;

public class XmlSearchException extends Exception
{
    public XmlSearchException() {
        super();
    }

    public XmlSearchException(String s) {
        super(s);
    }

    public XmlSearchException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public XmlSearchException(Throwable throwable) {
        super(throwable);
    }
}
